"use client"

import { useState } from "react"
import { motion } from "framer-motion"
import Link from "next/link"
import {
  FileText,
  MapPin,
  AlertTriangle,
  CheckCircle,
  Download,
  MessageSquare,
  ChevronRight,
  Info,
  Clock,
  Scale,
  Bookmark,
  Share2,
  RefreshCcw,
  Eye,
} from "lucide-react"
import { Button } from "@/components/ui/button"
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card"
import { Badge } from "@/components/ui/badge"
import { Tabs, TabsContent, TabsList, TabsTrigger } from "@/components/ui/tabs"
import { Accordion, AccordionContent, AccordionItem, AccordionTrigger } from "@/components/ui/accordion"
import { RiskMeter } from "@/components/analyze/risk-meter"
import { LegalTimeline } from "@/components/analyze/legal-timeline"
import { NextSteps } from "@/components/analyze/next-steps"
import { StateComparisonModal } from "@/components/analyze/state-comparison-modal"
import { toast } from "sonner"
import type { DocumentAnalysis } from "@/lib/types"

interface ResultsDashboardProps {
  fileName: string
  onNewAnalysis: () => void
  analysis: DocumentAnalysis
}

function renderDetailValue(value: unknown): string {
  if (value === null || value === undefined) return "Not specified"
  if (typeof value === "string") return value
  if (typeof value === "number" || typeof value === "boolean") return String(value)
  if (typeof value === "object") {
    // Handle objects like { complainant, accused }
    return Object.entries(value)
      .filter(([_, v]) => v)
      .map(([k, v]) => `${k}: ${v}`)
      .join(", ")
  }
  return String(value)
}

export function ResultsDashboard({ fileName, onNewAnalysis, analysis }: ResultsDashboardProps) {
  const [activeTab, setActiveTab] = useState("summary")
  const [showPlainEnglish, setShowPlainEnglish] = useState(true)
  const [isSaved, setIsSaved] = useState(false)

  const handleSave = () => {
    const savedCases = JSON.parse(localStorage.getItem("legalease-saved-cases") || "[]")
    const newCase = {
      id: Date.now().toString(),
      fileName,
      analysis,
      savedAt: new Date().toISOString(),
    }
    savedCases.push(newCase)
    localStorage.setItem("legalease-saved-cases", JSON.stringify(savedCases))
    setIsSaved(true)
    toast.success("Case saved to your device", {
      description: "You can access it from the Saved Cases page",
    })
  }

  const handleShare = async () => {
    try {
      await navigator.clipboard.writeText(window.location.href)
      toast.success("Link copied to clipboard")
    } catch {
      toast.error("Unable to copy link")
    }
  }

  const handleExport = () => {
    const exportContent = `
LegalEase AI - Document Analysis Report
========================================
File: ${fileName}
Document Type: ${analysis.documentType}
State/Jurisdiction: ${analysis.state}
Risk Level: ${analysis.riskLevel.toUpperCase()} (${analysis.riskScore}/100)

VERDICT
-------
${analysis.verdict}

KEY DETAILS
-----------
${Object.entries(analysis.extractedDetails)
  .filter(([_, value]) => value)
  .map(([key, value]) => `${key}: ${renderDetailValue(value)}`)
  .join("\n")}

LEGAL SECTIONS
--------------
${analysis.sections
  .map(
    (s) => `
${s.title} [${s.risk.toUpperCase()} RISK]
${s.plainText}
Penalty: ${s.penalty}
State Note: ${s.stateNote}
`,
  )
  .join("\n")}

${analysis.missingInfo.length > 0 ? `MISSING INFORMATION\n-------------------\n${analysis.missingInfo.map((i) => `- ${i}`).join("\n")}` : ""}

DISCLAIMER: This is informational only and does not constitute legal advice.
Generated by LegalEase AI on ${new Date().toLocaleDateString()}
    `.trim()

    const blob = new Blob([exportContent], { type: "text/plain" })
    const url = URL.createObjectURL(blob)
    const a = document.createElement("a")
    a.href = url
    a.download = `legalease-analysis-${Date.now()}.txt`
    a.click()
    URL.revokeObjectURL(url)

    toast.success("Report exported successfully")
  }

  return (
    <div className="max-w-5xl mx-auto pb-20 md:pb-0">
      {/* Header Actions */}
      <motion.div
        initial={{ opacity: 0, y: -10 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ duration: 0.3 }}
        className="flex flex-col sm:flex-row items-start sm:items-center justify-between gap-4 mb-6"
      >
        <div>
          <h1 className="text-2xl md:text-3xl font-bold tracking-tight mb-1">Analysis Results</h1>
          <p className="text-muted-foreground flex items-center gap-2">
            <FileText className="h-4 w-4" />
            {fileName}
          </p>
        </div>
        <div className="flex flex-wrap items-center gap-2">
          <Button
            variant="outline"
            size="sm"
            className={`gap-2 bg-transparent ${isSaved ? "text-primary" : ""}`}
            onClick={handleSave}
          >
            <Bookmark className={`h-4 w-4 ${isSaved ? "fill-current" : ""}`} />
            <span className="hidden sm:inline">{isSaved ? "Saved" : "Save"}</span>
          </Button>
          <Button variant="outline" size="sm" className="gap-2 bg-transparent" onClick={handleShare}>
            <Share2 className="h-4 w-4" />
            <span className="hidden sm:inline">Share</span>
          </Button>
          <Button variant="outline" size="sm" className="gap-2 bg-transparent" onClick={handleExport}>
            <Download className="h-4 w-4" />
            Export
          </Button>
          <Button size="sm" className="gap-2" onClick={onNewAnalysis}>
            <RefreshCcw className="h-4 w-4" />
            New
          </Button>
        </div>
      </motion.div>

      {/* Summary Banner */}
      <motion.div
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ delay: 0.1, duration: 0.4 }}
      >
        <Card className="mb-6 overflow-hidden">
          <div className="bg-gradient-to-r from-primary/10 via-accent/5 to-primary/10 p-4 sm:p-6">
            <div className="grid md:grid-cols-3 gap-6">
              {/* Document Info */}
              <div className="flex items-start gap-4">
                <div className="h-12 w-12 sm:h-14 sm:w-14 rounded-xl bg-background/80 flex items-center justify-center shrink-0">
                  <FileText className="h-6 w-6 sm:h-7 sm:w-7 text-primary" />
                </div>
                <div>
                  <div className="text-sm text-muted-foreground mb-1">Document Type</div>
                  <div className="font-semibold text-base sm:text-lg">{analysis.documentType}</div>
                  <div className="flex items-center gap-1.5 text-sm text-muted-foreground mt-1">
                    <MapPin className="h-3.5 w-3.5" />
                    {analysis.state}
                  </div>
                </div>
              </div>

              {/* Risk Meter */}
              <div className="flex flex-col items-center justify-center">
                <RiskMeter level={analysis.riskLevel} score={analysis.riskScore} />
              </div>

              {/* Quick Verdict */}
              <div className="flex items-center">
                <div className="bg-accent/10 border border-accent/20 rounded-xl p-4 w-full">
                  <div className="flex items-center gap-2 mb-2">
                    <CheckCircle className="h-5 w-5 text-accent" />
                    <span className="font-semibold">Quick Verdict</span>
                  </div>
                  <p className="text-sm text-muted-foreground">{analysis.verdict}</p>
                </div>
              </div>
            </div>
          </div>
        </Card>
      </motion.div>

      {/* Main Content Tabs */}
      <motion.div
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ delay: 0.2, duration: 0.4 }}
      >
        <Tabs value={activeTab} onValueChange={setActiveTab}>
          <TabsList className="grid w-full grid-cols-4 mb-6">
            <TabsTrigger value="summary" className="gap-2 text-xs sm:text-sm">
              <FileText className="h-4 w-4" />
              <span className="hidden sm:inline">Summary</span>
            </TabsTrigger>
            <TabsTrigger value="details" className="gap-2 text-xs sm:text-sm">
              <Eye className="h-4 w-4" />
              <span className="hidden sm:inline">Details</span>
            </TabsTrigger>
            <TabsTrigger value="timeline" className="gap-2 text-xs sm:text-sm">
              <Clock className="h-4 w-4" />
              <span className="hidden sm:inline">Timeline</span>
            </TabsTrigger>
            <TabsTrigger value="next-steps" className="gap-2 text-xs sm:text-sm">
              <CheckCircle className="h-4 w-4" />
              <span className="hidden sm:inline">Next Steps</span>
            </TabsTrigger>
          </TabsList>

          <TabsContent value="summary" className="space-y-6">
            {/* Key Extracted Details */}
            <Card>
              <CardHeader>
                <CardTitle className="text-lg">Key Details</CardTitle>
                <CardDescription>Information extracted from your document</CardDescription>
              </CardHeader>
              <CardContent>
                <div className="grid sm:grid-cols-2 gap-4">
                  {Object.entries(analysis.extractedDetails)
                    .filter(
                      ([_, value]) =>
                        value &&
                        renderDetailValue(value) !== "Not found" &&
                        renderDetailValue(value) !== "Not specified",
                    )
                    .map(([key, value]) => (
                      <div key={key} className="flex items-center justify-between p-3 rounded-lg bg-muted/50">
                        <span className="text-sm text-muted-foreground capitalize">
                          {key.replace(/([A-Z])/g, " $1").trim()}
                        </span>
                        <span className="font-medium text-sm">{renderDetailValue(value)}</span>
                      </div>
                    ))}
                </div>

                {analysis.missingInfo.length > 0 && (
                  <div className="mt-4 p-4 rounded-lg bg-amber-500/10 border border-amber-500/20">
                    <div className="flex items-center gap-2 mb-2">
                      <AlertTriangle className="h-4 w-4 text-amber-500" />
                      <span className="font-medium text-sm">Missing Information</span>
                    </div>
                    <ul className="space-y-1">
                      {analysis.missingInfo.map((info, i) => (
                        <li key={i} className="text-sm text-muted-foreground flex items-start gap-2">
                          <span className="text-amber-500">â€¢</span>
                          {info}
                        </li>
                      ))}
                    </ul>
                  </div>
                )}
              </CardContent>
            </Card>

            {/* Legal Sections */}
            <Card>
              <CardHeader>
                <div className="flex flex-col sm:flex-row sm:items-center justify-between gap-3">
                  <div>
                    <CardTitle className="text-lg">Legal Sections</CardTitle>
                    <CardDescription>Applicable laws and their explanations</CardDescription>
                  </div>
                  <Button
                    variant="outline"
                    size="sm"
                    onClick={() => setShowPlainEnglish(!showPlainEnglish)}
                    className="w-fit bg-transparent"
                  >
                    {showPlainEnglish ? "Show Legal Text" : "Show Plain English"}
                  </Button>
                </div>
              </CardHeader>
              <CardContent>
                {analysis.sections.length > 0 ? (
                  <Accordion type="single" collapsible className="w-full">
                    {analysis.sections.map((section) => (
                      <AccordionItem key={section.id} value={section.id}>
                        <AccordionTrigger className="hover:no-underline">
                          <div className="flex items-center gap-3 text-left">
                            <Badge
                              variant={
                                section.risk === "high"
                                  ? "destructive"
                                  : section.risk === "medium"
                                    ? "outline"
                                    : "secondary"
                              }
                            >
                              {section.risk} risk
                            </Badge>
                            <span className="font-medium text-sm sm:text-base">{section.title}</span>
                          </div>
                        </AccordionTrigger>
                        <AccordionContent>
                          <div className="space-y-4 pt-2">
                            <div className="p-4 rounded-lg bg-muted/50">
                              <div className="text-sm font-medium mb-2">
                                {showPlainEnglish ? "What this means:" : "Legal text:"}
                              </div>
                              <p className="text-sm text-muted-foreground">
                                {showPlainEnglish ? section.plainText : section.legalText}
                              </p>
                            </div>

                            <div className="grid sm:grid-cols-2 gap-4">
                              <div className="p-4 rounded-lg bg-primary/5">
                                <div className="flex items-center gap-2 text-sm font-medium mb-2">
                                  <Scale className="h-4 w-4 text-primary" />
                                  Penalty
                                </div>
                                <p className="text-sm text-muted-foreground">{section.penalty}</p>
                              </div>
                              <div className="p-4 rounded-lg bg-accent/5">
                                <div className="flex items-center gap-2 text-sm font-medium mb-2">
                                  <MapPin className="h-4 w-4 text-accent" />
                                  State-Specific Note
                                </div>
                                <p className="text-sm text-muted-foreground">{section.stateNote}</p>
                              </div>
                            </div>

                            <div className="flex flex-wrap gap-2">
                              <Link
                                href={`/chat?question=${encodeURIComponent(`Explain ${section.title} in simpler terms`)}`}
                              >
                                <Button variant="outline" size="sm" className="gap-2 bg-transparent">
                                  <MessageSquare className="h-4 w-4" />
                                  Ask AI to simplify
                                </Button>
                              </Link>
                              <StateComparisonModal
                                currentState={analysis.state}
                                section={section.id}
                                sectionTitle={section.title}
                              />
                            </div>
                          </div>
                        </AccordionContent>
                      </AccordionItem>
                    ))}
                  </Accordion>
                ) : (
                  <div className="text-center py-8 text-muted-foreground">
                    <p>No specific legal sections were identified in this document.</p>
                  </div>
                )}
              </CardContent>
            </Card>
          </TabsContent>

          <TabsContent value="details">
            <Card>
              <CardHeader>
                <CardTitle>Document Analysis Summary</CardTitle>
                <CardDescription>Overview of extracted information</CardDescription>
              </CardHeader>
              <CardContent>
                <div className="space-y-6">
                  <div className="p-4 rounded-lg bg-muted/50">
                    <h3 className="font-semibold mb-3">Document Type</h3>
                    <p className="text-muted-foreground">{analysis.documentType}</p>
                  </div>

                  <div className="p-4 rounded-lg bg-muted/50">
                    <h3 className="font-semibold mb-3">Jurisdiction</h3>
                    <p className="text-muted-foreground">{analysis.state}</p>
                  </div>

                  <div className="p-4 rounded-lg bg-muted/50">
                    <h3 className="font-semibold mb-3">All Extracted Details</h3>
                    <div className="space-y-2">
                      {Object.entries(analysis.extractedDetails).map(([key, value]) => (
                        <div key={key} className="flex justify-between text-sm">
                          <span className="text-muted-foreground capitalize">
                            {key.replace(/([A-Z])/g, " $1").trim()}:
                          </span>
                          <span className="font-medium">{renderDetailValue(value)}</span>
                        </div>
                      ))}
                    </div>
                  </div>

                  <div className="p-4 rounded-lg bg-primary/5 border border-primary/20">
                    <h3 className="font-semibold mb-3">AI Summary</h3>
                    <p className="text-muted-foreground">{analysis.verdict}</p>
                  </div>
                </div>
              </CardContent>
            </Card>
          </TabsContent>

          <TabsContent value="timeline">
            <LegalTimeline timeline={analysis.timeline} />
          </TabsContent>

          <TabsContent value="next-steps">
            <NextSteps steps={analysis.nextSteps} />
          </TabsContent>
        </Tabs>
      </motion.div>

      {/* AI Chat CTA */}
      <motion.div
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ delay: 0.3, duration: 0.4 }}
        className="mt-8"
      >
        <Card className="bg-gradient-to-r from-primary/5 to-accent/5 border-primary/20">
          <CardContent className="p-4 sm:p-6">
            <div className="flex flex-col sm:flex-row items-center justify-between gap-4">
              <div className="flex items-center gap-4 text-center sm:text-left">
                <div className="h-12 w-12 rounded-xl bg-primary/10 flex items-center justify-center shrink-0">
                  <MessageSquare className="h-6 w-6 text-primary" />
                </div>
                <div>
                  <h3 className="font-semibold">Have Questions?</h3>
                  <p className="text-sm text-muted-foreground">
                    Ask our AI assistant for more details about your document
                  </p>
                </div>
              </div>
              <Link href="/chat">
                <Button className="gap-2 w-full sm:w-auto">
                  <MessageSquare className="h-4 w-4" />
                  Ask AI Assistant
                  <ChevronRight className="h-4 w-4" />
                </Button>
              </Link>
            </div>

            {analysis.suggestedQuestions && analysis.suggestedQuestions.length > 0 && (
              <div className="mt-4 pt-4 border-t border-border/50">
                <p className="text-sm text-muted-foreground mb-2">Suggested questions:</p>
                <div className="flex flex-wrap gap-2">
                  {analysis.suggestedQuestions.slice(0, 3).map((q, i) => (
                    <Link key={i} href={`/chat?question=${encodeURIComponent(q)}`}>
                      <Button variant="outline" size="sm" className="text-xs bg-transparent">
                        {q}
                      </Button>
                    </Link>
                  ))}
                </div>
              </div>
            )}
          </CardContent>
        </Card>
      </motion.div>

      {/* Disclaimer */}
      <div className="mt-6 text-center text-sm text-muted-foreground">
        <p className="flex items-center justify-center gap-2">
          <Info className="h-4 w-4" />
          This is informational only and does not constitute legal advice.
        </p>
      </div>
    </div>
  )
}
